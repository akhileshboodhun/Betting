<h2>History</h2>
<p class="bg-light">
L’hippisme à L’Ile Maurice prend naissance en 1812. Les premières courses eurent lieu au Champ de Mars le 25 juin de cette année, 
et furent le début de 200 ans de compétition entre propriétaires et écuries, qui se poursuivait même durant les deux guerres mondiales.
L’objectif des fondateurs du MTC(Mauritius Turf Club), était d’amener une réconciliation entre les colons français et l’administration anglaise, 
car les Anglais avaient conquis l’île en décembre 1810. Ils étaient convaincus que l’ambiance conviviale des courses hippiques aiderait à 
promouvoir l’unité entre ces deux communautés ainsi que la paix sociale et l’harmonie après des années de guerre dans l’Océan Indien. 
Le nouveau gouverneur, Sir Robert Farquhar et son épouse d’origine française, Maria Lautour, soutinrent activement l’organisation de 
courses hippiques, et Mme Farquhar offrit la première coupe d’or de l’histoire des courses locales à cette organisation.
Dès son arrivée à Maurice, le Colonel Draper apporta un soutien enthousiaste à la promotion des courses au Champ de Mars durant une 
vingtaine d’années, faisant courir et montant ses propres chevaux, à tel point qu’il devint, aux yeux de ses contemporains, le « père » du Mauritius Turf Club.

 </p>
<h2>International Relations</h2>
<p>
The Mauritius Turf Club is a member of the International Federation of Horse-Racing Authorities. As such it is the sole organiser of 
horse racing in Mauritius. The Federation holds its annual meeting in Paris in October at the time of the running of the Prix de l’Arc de Triomphe.
In 1997, at the 25th Conference of The Asian Racing Federation, The MTC became a member of the ARF. This Federation which is 
made up of 24 countries meets every two years and its Secretariat is located in Sydney.
</p>
